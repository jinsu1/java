public class Ex01_변수의_사용 {
    public static void main(String[] args) {
        int a;
        a = 100;
        System.out.println(a);

        int  b = a;
        System.out.println(b);

        double c = 3.14;
        System.out.println(c);

        boolean d = true;
        System.out.println(d);
    }
}