public class Ex02_리터럴 {
    public static void main(String[] args) {
        System.out.println(100);

        int num1 = 100;
        System.out.println(num1);

        double num2 = 100;
        System.out.println(num2);

        float num3 = 3.14F;
        System.out.println(num3);

        double num4 = 3.14D;
        System.out.println(num4);

        double num5 = 100D;
        System.out.println(num5);
    }
}
